void solve(){
    ll n,k;
    cin>>k>>n;
    vector <ll> ans(k,1);
    for (int i=1; i<k; i++) {
        ans[i] = ans[i-1] + i;
    }
    ll iter = k-1;
    while (ans[iter] > n) {
        ans[iter--] = n--;
    }
    for (auto d:ans) cout<<d<<" "; cout<<"\n";
}

