void solve(){
    ll n;
    cin>>n;
    vector <ll> v(n);
    for (int i=0; i<n; i++) cin>>v[i]; 
    ll a = (1<<31)-1;
    map <ll,ll> m;
    ll ans = 0;
    for (int i=0; i<n; i++){
        if (m[a ^ v[i]]) m[a ^ v[i]]--;
        else {
            m[v[i]]++;
            ans++;
        }
    }
    cout<<ans<<"\n";
}

