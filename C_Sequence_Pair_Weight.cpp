void solve(){
    ll n;
    cin>>n;
    vector <ll> v(n);
    for (int i=0; i<n; i++)cin>>v[i];
    map <ll,ll> m;
    vector <ll> dp(n, 0);
    ll ans = 0;
    for (int i=0; i<n; i++){
        if (i>0)dp[i] += dp[i-1];
        dp[i]+=m[v[i]];
        m[v[i]]+=(i+1);
        ans += dp[i];
    }
    cout<<ans<<"\n";
}

