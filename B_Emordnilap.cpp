ll fact(ll n){
    ll ans = 1;
    for (int i=2; i<=n; i++){
        ans = (ans*i)%mod;
    }
    return ans;
}

void solve(){
    ll n;
    cin>>n;
    cout<< (((n * (n-1)) % mod) * fact(n)) % mod <<"\n";
}

