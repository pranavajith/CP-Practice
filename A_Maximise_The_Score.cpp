void solve(){
    ll n;
    cin>>n;
    vector <ll> v(2*n);
    for (int i=0; i<2*n; i++) cin>>v[i];
    sort(v.begin(), v.end());
    ll a = 2*n-2;
    ll ans = 0;
    while (a>=0) {
        ans += v[a];
        a-=2;
    }
    cout<<ans<<"\n";
}

