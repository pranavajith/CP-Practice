void solve(){
    ll n, sum1=0;
    cin>>n;
    vector <ll> v;
    for (int i=0;i<n;i++){
        ll a;
        cin>>a;
        v.push_back(a);
        sum1+=abs(a);
    }
    ll count1=0;
    for (int i=0;i<n;i++){
        if (v[i]<0){
            count1++;
            // a=i;
            while (i<n && v[i]<=0){
                i++;
            }
        }
    }
    cout<<sum1<<" "<<count1<<"\n";
}

